package ex01;

public class HelloJava
{
	public static void main(String[] args)
	{
		// ★★★ sysou -> 컨트롤+쉬프트 = [자동완성 기능] -> 엔터
		
		System.out.println("Hello JAVA"); //println은 줄 단위로 "내용"을 출력한다.
		System.out.print("오늘 날을 자바서 "); //print은 "내용"을 출력한다.
		
		//print는 자동으로 줄 바꿔주지 않으므로 
				// (\n) 줄바꿈을 넣는다
		System.out.print("JAVA를 자바?\n"); 		
		System.out.println("대한민국의 가치있는 개발자 1인이 되고 싶어요!");
		
	}
}
