public class Ex03_SwitchNoneBreak
{
	public static void main(String[] args)
	{
		int n = 6;	
		switch (n % 7)
		{
//		한줄로 써도 상관은 없다.
//		여러 case를 동시에 처리할 때는 case를 나열
//		if문에서 || (논리or)을 사용한 것과 비슷
		case 1:case 2:
		case 3:
		case 4:
		case 5:
			System.out.println("주중"); //월화수목금(1~5) 주중
			break; // 여기서 멈춤
		case 6:
		default:
			System.out.println("주말"); //토일(6~7) 주말로 출력
		}
	}
}
